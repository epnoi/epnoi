package org.epnoi.model;

public class Content {
	String content;
	String format;

	// ----------------------------------------------------------------------------------------------
	
	public String getContent() {
		return content;
	}
		
	// ----------------------------------------------------------------------------------------------

	public void setContent(String content) {
		this.content = content;
	}

	// ----------------------------------------------------------------------------------------------
	
	public String getFormat() {
		return format;
	}

	// ----------------------------------------------------------------------------------------------
	
	public void setFormat(String format) {
		this.format = format;
	}
}
